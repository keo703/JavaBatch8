package com.syntax.class04;

public class IfElseif {

	public static void main(String[] args) {
	
		int day=2;
		
		if(day==1) {
			System.out.println("Today is Monday");
		}else if (day==2) {
			System.out.println("Today is Tuesday");}
		else if (day==3) {
			System.out.println("Today is Wednesday");}

	}

}
