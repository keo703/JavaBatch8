package com.syntax.class02;

public class VariableValues {

	public static void main(String[] args) {
	
		
		byte mylittle;
		mylittle = 5;
		System.out.println(mylittle);
		
		short medium;
		medium= 2506;
		System.out.println(medium);
		
		int mynum23;
		mynum23= 20000;
		System.out.println(mynum23);
		
		float numberf;
		numberf= 20040f;
		System.out.println(numberf);
		
		double numberper;
		numberper=204.44;
		System.out.println(numberper);
		
		char letter;
		letter= 's';
		System.out.println(letter);
		char number2;
		number2= 'f';
		System.out.println(number2);
		
		boolean yesorno;
		yesorno=false;
		System.out.println(yesorno);
				
	}

}
